class TestOracleConnection:
    def test_connect_oracle(self):
        _connect("ora_conn")
        try:
            result = call_tool(
                "execute_query",
                {"name": "ora_conn", "query": "SELECT 1 AS ok FROM DUAL"},
            )
            assert "1" in str(result), f"Connection not usable: {result}"
        finally:
            call_tool("disconnect_database", {"name": "ora_conn"})

    def test_list_shows_oracle(self):
        _connect("ora_ls")
        try:
            result = call_tool("list_databases", {})
            assert "ora_ls" in str(
                result
            ), f"ora_ls not in list_databases after connect: {result}"
        finally:
            call_tool("disconnect_database", {"name": "ora_ls"})

    def test_disconnect(self):
        _connect("ora_dc")
        call_tool("disconnect_database", {"name": "ora_dc"})
        result = call_tool("list_databases", {})
        assert "ora_dc" not in str(
            result
        ), f"ora_dc still listed after disconnect: {result}"

    def test_duplicate_connect_rejected(self):
        _connect("ora_dup")
        try:
            result = call_tool(
                "connect_database",
                {"name": "ora_dup", "db_type": "oracle", "conn_string": ORACLE_URL},
            )
            result_str = str(result)
            assert (
                "already" in result_str.lower() or "error" in result_str.lower()
            ), f"Expected duplicate-name error: {result_str}"
        finally:
            call_tool("disconnect_database", {"name": "ora_dup"})


