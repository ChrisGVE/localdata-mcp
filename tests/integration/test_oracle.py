class TestOracleQueries:
    def test_select(self):
        _connect("ora_sel")
        try:
            result = call_tool(
                "execute_query",
                {
                    "name": "ora_sel",
                    "query": ("SELECT name, email FROM test_data WHERE ROWNUM <= 10"),
                },
            )
            result_str = str(result)
            assert "user_" in result_str, f"Expected user rows: {result_str}"
        finally:
            call_tool("disconnect_database", {"name": "ora_sel"})

    def test_where_filter(self):
        _connect("ora_whr")
        try:
            result = call_tool(
                "execute_query",
                {
                    "name": "ora_whr",
                    "query": (
                        "SELECT name, email, category "
                        "FROM test_data WHERE category = 'A'"
                    ),
                },
            )
            result_str = str(result)
            assert "user_" in result_str, f"Expected user rows: {result_str}"
        finally:
            call_tool("disconnect_database", {"name": "ora_whr"})

    def test_aggregation(self):
        _connect("ora_agg")
        try:
            result = call_tool(
                "execute_query",
                {
                    "name": "ora_agg",
                    "query": (
                        "SELECT category, COUNT(*) AS cnt, "
                        "SUM(amount) AS total "
                        "FROM test_data GROUP BY category"
                    ),
                },
            )
            result_str = str(result)
            assert (
                "category" in result_str.lower() or "A" in result_str
            ), f"Aggregation result missing expected data: {result_str}"
        finally:
            call_tool("disconnect_database", {"name": "ora_agg"})

    def test_join(self):
        _connect("ora_join")
        try:
            result = call_tool(
                "execute_query",
                {
                    "name": "ora_join",
                    "query": (
                        "SELECT a.name, b.category "
                        "FROM test_data a "
                        "INNER JOIN test_data b ON a.id = b.id "
                        "WHERE a.category = 'A' AND ROWNUM <= 5"
                    ),
                },
            )
            result_str = str(result)
            assert "error" not in result_str.lower(), f"Join query failed: {result_str}"
        finally:
            call_tool("disconnect_database", {"name": "ora_join"})


