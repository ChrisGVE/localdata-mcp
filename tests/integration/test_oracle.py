def _connect(name):
    """Connect to Oracle via MCP and assert success."""
    result = call_tool(
        "connect_database",
        {"name": name, "db_type": "oracle", "conn_string": ORACLE_URL},
    )
    assert "error" not in str(result).lower(), f"Connection failed: {result}"


