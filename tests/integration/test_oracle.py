class TestOracleErrors:
    def test_bad_url(self):
        result = call_tool(
            "connect_database",
            {
                "name": "ora_bad",
                "db_type": "oracle",
                "conn_string": (
                    "oracle+oracledb://baduser:badpass@localhost:19999"
                    "/?service_name=NOPE"
                ),
            },
        )
        try:
            result_str = str(result)
            assert (
                "error" in result_str.lower() or "fail" in result_str.lower()
            ), f"Expected error for bad URL: {result_str}"
        finally:
            try:
                call_tool("disconnect_database", {"name": "ora_bad"})
            except Exception:
                pass

    def test_disconnect_nonexistent(self):
        result = call_tool("disconnect_database", {"name": "ora_nonexist_xyz"})
        result_str = str(result)
        assert (
            "error" in result_str.lower() or "not" in result_str.lower()
        ), f"Expected error for nonexistent disconnect: {result_str}"

    def test_query_nonexistent_connection(self):
        result = call_tool(
            "execute_query",
            {"name": "ora_nonexist_xyz", "query": "SELECT 1 FROM DUAL"},
        )
        result_str = str(result)
        assert (
            "error" in result_str.lower() or "not" in result_str.lower()
        ), f"Expected error for nonexistent query: {result_str}"
